        <section id="objectives">
            <h2>Objectives of BCA</h2>
            <ul>
                <li>To produce professionals in the field as programmers and software developers.</li>
                <li>To provide knowledge about various tools and techniques used in software development.</li>
                <li>To provide pupils with both practical and theoretical aspects of computer application studies.</li>
                <li>To enhance students' skills to create solutions to technical problems in industrial, commercial, and government sectors.</li>
                <li>To provide them with a solid foundation for further studies in disciplines such as Master of Information Technology (MIT), Master of Business Administration (MBA), Master of Computer Applications (MCA), and other related fields.</li>
            </ul>
        </section>

        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>
                Students from all faculties are eligible to get admission to the BCA course at TU. Graduates from PCL, +2, or equivalent in any discipline, who have passed with a minimum of 40% marks or a 'C' grade or a 2 CGPA, or a C grade in all subjects of 11th and 12th, are eligible for admission.
            </p>
        </section>

        <section id="fee-structure">
            <h2>Fee Structure</h2>
            <p>
                The BCA fee structure at TU ranges from Rs. 400,000 to 1,200,000. Candidates can also apply for a scholarship scheme based on a merit list.
            </p>
        </section>

        <section id="career-scope">
            <h2>Career and Scope</h2>
            <p>
                With the increasing reliance on computers in our daily lives, there is a growing demand for skilled professionals in this field. BCA program graduates possess the qualifications and knowledge to meet this demand and pursue various career opportunities. Graduates can work as software developers, system analysts, project managers, database administrators, system designers, and programmers in various industries. They can also pursue higher studies in related fields such as MCA, MBA, or other relevant postgraduate degrees.
            </p>
