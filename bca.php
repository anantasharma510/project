            <p>
                Tribhuvan University introduced a new program called Bachelor of Computer Application (BCA) under the faculty of Humanities and Social Sciences. BCA course was proposed by TU starting from the academic year 2071/72 BS, however, until late December 2016, TU did not provide affiliation to any of its constituent or private colleges. This course was officially launched from the academic year 2074/2075. In the initial phase, Tribhuvan University granted permission to run this program in six of its constituent campuses.
